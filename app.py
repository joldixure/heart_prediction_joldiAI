import streamlit as st
import pandas as pd
import numpy as np
import joblib
import tensorflow as tf

# -------------------------------
# Page config
# -------------------------------
st.set_page_config(
    page_title="Heart Disease Decision Support",
    page_icon="‚ù§Ô∏è",
    layout="centered"
)

# -------------------------------
# Load model and scaler
# -------------------------------
model = tf.keras.models.load_model("model/heart_model.keras")
scaler = joblib.load("model/scaler.pkl")

# -------------------------------
# Header section
# -------------------------------
st.image("assets/heart_banner.jpg", use_column_width=True)

st.title("Heart Disease Decision Support System")
st.markdown(
    """
    This application estimates the **risk of heart disease** based on clinical data.
    
    ‚ö†Ô∏è *For educational and decision-support purposes only.*
    """
)

st.divider()

# -------------------------------
# Input section
# -------------------------------
st.header("Patient Information")

col1, col2 = st.columns(2)

with col1:
    age = st.slider("Age", 18, 100, 50)
    sex = st.radio("Sex", ["M", "F"])
    chest_pain = st.selectbox(
        "Chest Pain Type",
        ["ATA", "NAP", "ASY", "TA"]
    )
    resting_bp = st.slider("Resting Blood Pressure", 80, 200, 120)
    cholesterol = st.slider("Cholesterol", 100, 500, 200)

with col2:
    fasting_bs = st.radio("Fasting Blood Sugar > 120 mg/dl", [0, 1])
    resting_ecg = st.selectbox("Resting ECG", ["Normal", "ST", "LVH"])
    max_hr = st.slider("Max Heart Rate", 60, 220, 150)
    exercise_angina = st.radio("Exercise-Induced Angina", ["Y", "N"])
    oldpeak = st.slider("Oldpeak", 0.0, 6.0, 1.0)
    st_slope = st.selectbox("ST Slope", ["Up", "Flat", "Down"])

# -------------------------------
# Build input dataframe
# -------------------------------
input_data = pd.DataFrame({
    "Age": [age],
    "Sex": [sex],
    "ChestPainType": [chest_pain],
    "RestingBP": [resting_bp],
    "Cholesterol": [cholesterol],
    "FastingBS": [fasting_bs],
    "RestingECG": [resting_ecg],
    "MaxHR": [max_hr],
    "ExerciseAngina": [exercise_angina],
    "Oldpeak": [oldpeak],
    "ST_Slope": [st_slope]
})

# -------------------------------
# Preprocessing
# -------------------------------
training_columns = scaler.feature_names_in_

input_encoded = pd.get_dummies(input_data)
input_encoded = input_encoded.reindex(columns=training_columns, fill_value=0)

input_scaled = scaler.transform(input_encoded)

st.divider()

# -------------------------------
# Prediction
# -------------------------------
if st.button("üîç Predict Risk", use_container_width=True):
    prob = model.predict(input_scaled)[0][0]

    st.subheader("Prediction Result")

    st.progress(float(prob))
    st.write(f"**Estimated Risk Probability:** `{prob:.2f}`")

    if prob >= 0.5:
        st.error("‚ö†Ô∏è High risk of heart disease detected")
    else:
        st.success("‚úÖ Low risk of heart disease detected")

    st.caption(
        "This result is generated by a machine learning model and should not replace medical advice."
    )
